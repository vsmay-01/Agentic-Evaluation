from pydantic import BaseModel, Field
from typing import List, Optional

class EvaluationInput(BaseModel):
    prompt: str = Field(..., description="The prompt/instruction given to the agent")
    agent_response: str = Field(..., description="The actual response generated by the agent")
    reference: Optional[str] = Field(None, description="Optional reference/expected answer for accuracy comparison")

class EvaluationRequest(BaseModel):
    id: str
    model_name: str
    inputs: List[EvaluationInput]
